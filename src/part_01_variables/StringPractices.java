package part_01_variables;

public class StringPractices {
    public static void main(String[] args) {

        byte MyByte = -16;
        System.out.println("myByte = " + MyByte);

        short myShort;
        myShort = 250;
        System.out.println("myShort = " + myShort);
        myShort = -345;
        System.out.println("myShort = " + myShort);

        int _myInt = 1231231235;
        System.out.println("myInt = " + _myInt);

        long $myLong = 234234234234773L;
        System.out.println("myLong = " + $myLong);

        var my_Age = 35;
        var myName = "Alex";

        System.out.println(my_Age);

        float my$Float = 34.5f;
        System.out.println("myFloat = " + my$Float);
        double myDouble = 43534535345345.553545;
        System.out.println("myDouble = " + myDouble);

        boolean myTrue = true;
        System.out.println("myTrue = " + myTrue);
        boolean myFalse = false;

        char myChar = 't';
        char myChar2 = '\u1424';
        System.out.println("myChar = " + myChar);
        System.out.println("myChar2 = " + myChar2);


    }
}


